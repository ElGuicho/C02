int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			i++;
		}
		if ('a' <= str[i] && str[i] <= 'z')
		{
			i++;
		}
		else
		{
			i = 0;
			return (i);
		}
	}
	i = 1;
	return (i);
}
